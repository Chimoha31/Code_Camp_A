// Resister (Create User)
router.post("/resister", async (req, res) => {
  try {
    const newUser = await new User({
      email: req.body.email,
      password: req.body.password,
    });
    const user = await newUser.save();
    return res.status(200).json({ msg: "Succesfully added user", data: user });
  } catch (err) {
    return res.status(500).json(err);
  }
});

// Login
router.post("/login", async (req, res) => {
  try {
    const user = await User.findOne({ email: req.body.email });
    if (!user) return res.status(404).send("There is no user...");

    const validPassword = req.body.password === user.password;
    if (!validPassword) return res.status(500).json("Not match password..");
    return res.status(200).json({ msg: "Matched Password", data: user });
  } catch (err) {
    return res.status(500).json(err);
  }
});

